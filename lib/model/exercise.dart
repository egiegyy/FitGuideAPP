class Exercise {
  final String name;
  final String image;
  final String equipment;
  final String category;
  final String level;
  final String reps;
  final String description;
  final List<String> steps;

  const Exercise({
    required this.name,
    required this.image,
    required this.equipment,
    required this.category,
    required this.level,
    required this.reps,
    required this.description,
    required this.steps,
  });
}
